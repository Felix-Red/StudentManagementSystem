    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = studentData.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "No row selected",
            "Select row", JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel model = (DefaultTableModel) studentData.getModel();
            model.removeRow(row);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
            FileInputStream file = new FileInputStream("myfile.bin");
            ObjectInputStream input = new ObjectInputStream(file);
            
            Vector<Vector> data = (Vector<Vector>) input.readObject();
            input.close();
            file.close();
            DefaultTableModel model = (DefaultTableModel) studentData.getModel();
            
            for(int i = 0; i < data.size(); i++){
                Vector row = data.get(i);
                model.addRow(new Object[]{row.get(0), row.get(1), row.get(2), row.get(3)});
            }
        }catch(Exception ex){
            ex.printStackTrace();
            
        }
            
    }//GEN-LAST:event_formWindowOpened

