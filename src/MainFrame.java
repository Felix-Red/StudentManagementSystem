    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) studentData.getModel();
        Vector<Vector> tableData = model.getDataVector();
        try{
            FileOutputStream file = new FileOutputStream("file.bin");
            ObjectOutputStream output = new ObjectOutputStream(file);
            output.writeObject(tableData);
            output.close();
            file.close();
            
            JOptionPane.showMessageDialog(this, "Data written to file", "Successful write", JOptionPane.ERROR_MESSAGE);            
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                // TODO add your handling code here:
                String id = tfID.getText();
                String name = tfName.getText();
                String surname = tfSurname.getText();
                String course = tfCourse.getText();
                
                if(id.isEmpty() || name.isEmpty() || surname.isEmpty() || course.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Please enter all fields", "Error",
                            JOptionPane.ERROR_MESSAGE);
                }else{
                    DefaultTableModel model = (DefaultTableModel) studentData.getModel();
                    model.addRow(new Object[]{id, name, surname, course});
                    
                        tfID.setText("");
                        tfName.setText("");
                        tfSurname.setText("");
                        tfCourse.setText("");
                       
                }
                
    }//GEN-LAST:event_jButton1ActionPerformed

